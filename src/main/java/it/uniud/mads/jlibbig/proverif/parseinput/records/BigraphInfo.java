package it.uniud.mads.jlibbig.proverif.parseinput.records;

/**
 * This class implement a record for the Integrative-Json's Bigraph Info
 */
public class BigraphInfo {

    private String id;

    /**
     * @param id
     */
    public BigraphInfo(String id) {
        this.id = id;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }  
}